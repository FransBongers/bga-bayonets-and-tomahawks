  }

  public function actEventStagedLacrosseGame($args)
  {
    self::checkAction('actEventStagedLacrosseGame');
    $useStagedLacrosseGame = $args['useStagedLacrosseGame'];

    // Only path to there should declare using it
    if (!$useStagedLacrosseGame) {
      throw new \feException("ERROR 068");
    }

    Notifications::message(clienttranslate('${player_name} uses Staged Lacrosse Game'), [
      'player' => self::getPlayer()
    ]);

    Globals::setUsedEventCount(INDIAN, 1);

    $raidResultionAction = $this->ctx->getParent()->getUnresolvedActions([RAID_RESOLUTION])[0];
    $raidResultionAction->updateInfo('useStagedLacrossGame', true);

    $this->resolveAction($args);
  }

