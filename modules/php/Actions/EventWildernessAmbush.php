  public function actPassEventWildernessAmbush()
  {
    $player = self::getPlayer();
    // Stats::incPassActionCount($player->getId(), 1);
    Notifications::message(clienttranslate('${player_name} does not use ${tkn_boldText_eventName}'), [
      'player' => $player,
      'tkn_boldText_eventName' => clienttranslate('Wilderness Ambush'),
      'i18n' => ['tkn_boldText_eventName']
    ]);

    $this->resolveAction(PASS);
  }

  public function actEventWildernessAmbush($args)
  {
    self::checkAction('actEventWildernessAmbush');
    $useWildernessAmbush = $args['useWildernessAmbush'];

    // Only path to there should declare using it
    if (!$useWildernessAmbush) {
      throw new \feException("ERROR 070");
    }

    $player = self::getPlayer();

    Notifications::message(clienttranslate('${player_name} uses ${tkn_boldText_eventName}'), [
      'player' => $player,
      'tkn_boldText_eventName' => clienttranslate('Wilderness Ambush'),
      'i18n' => ['tkn_boldText_eventName']
    ]);

    Globals::setUsedEventCount(FRENCH, 1);

    $space = Spaces::get($this->ctx->getInfo()['spaceId']);

    $lightUnitsCount = count(Utils::filter($space->getUnits(FRENCH), function ($unit) {
      return $unit->isLight();
    }));

    $this->moveBattleVictoryMarker($player, FRENCH, $lightUnitsCount >= 3 ? 2 : 1);

    $this->resolveAction($args);
  }

