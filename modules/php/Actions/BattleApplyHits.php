  public function argsBattleApplyHits()
  {
    $info = $this->ctx->getInfo();
    $unitIds = $info['unitIds'];
    $spaceId = $info['spaceId'];
    $faction = $info['faction'];
    $eliminate = isset($info['eliminate']) && $info['eliminate'];

    return [
      'units' => Units::getMany($unitIds)->toArray(),
      'spaceId' => $spaceId,
      'faction' => $faction,
      'eliminate' => $eliminate,
    ];
  }

