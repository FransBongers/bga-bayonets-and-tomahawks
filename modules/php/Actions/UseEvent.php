  public function actPassUseEvent()
  {
    $player = self::getPlayer();
    $stateArgs = $this->argsUseEvent();

    Notifications::message(clienttranslate('${player_name} does not use ${tkn_boldText_eventTitle}'), [
      'player' => $player,
      'tkn_boldText_eventTitle' => $stateArgs['eventTitle'],
      'i18n' => ['tkn_boldText_eventTitle']
    ]);

    $this->resolveAction(PASS);
  }

  public function actUseEvent($args)
  {
    self::checkAction('actUseEvent');
    $useEvent = $args['useEvent'];

    // Only path to there should declare using it
    if (!$useEvent) {
      throw new \feException("ERROR 071");
    }

    $stateArgs = $this->argsUseEvent();
    $player = self::getPlayer();

    Notifications::message(clienttranslate('${player_name} uses ${tkn_boldText_eventTitle}'), [
      'player' => $player,
      'tkn_boldText_eventTitle' => $stateArgs['eventTitle'],
      'i18n' => ['tkn_boldText_eventTitle']
    ]);

    $info = $this->ctx->getInfo();
    $cardId = $info['cardId'];
    $spaceId = isset($info['spaceId']) ? $info['spaceId'] : null;
    $space = $spaceId !== null ? Spaces::get($spaceId) : null;

    $card = Cards::get($cardId);

    $card->useEvent($player, $space);

    $this->resolveAction($args);
  }

